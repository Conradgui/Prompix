export const getDimensionLabel = (key: DimensionKey, locale: UiLocale): string => {
  return DIMENSION_LABELS[locale][key];
};

export const getModuleLabel = (moduleKey: string, locale: UiLocale): string => {
  return MODULE_LABELS[locale][moduleKey] || moduleKey;
};

export const formatLanguageName = (name: string | undefined, locale: UiLocale): string => {
  const normalized = (name || '').trim().toLowerCase();
  const isZh =
    normalized === 'zh' ||
    normalized === 'cn' ||
    normalized.includes('chinese') ||
    normalized.includes('中文');

  if (locale === 'zh') {
    return isZh ? '中文' : '英文';
  }
  return isZh ? 'Chinese' : 'English';
};
