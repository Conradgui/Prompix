export type UiLocale = 'zh' | 'en';

export const resolveUiLocale = (systemLanguage?: string): UiLocale => {
  const normalized = (systemLanguage || 'Chinese').trim().toLowerCase();
  if (
    normalized === 'zh' ||
    normalized === 'cn' ||
    normalized.includes('chinese') ||
    normalized.includes('中文')
  ) {
    return 'zh';
  }
  return 'en';
};

